        public static bool IsPermitRequired ( string systemName, decimal? x, decimal? y, decimal? z )
        {
            return PERMIT_SYSTEMS.Contains( systemName, StringComparer.InvariantCultureIgnoreCase ) ||
                   ( x != null && y != null && z != null && ( PERMIT_REGIONS
                       .OrderBy( r => Functions.StellarDistanceLy( r.X, r.Y, r.Z, x, y, z ) )
                       .FirstOrDefault( r => Functions.StellarDistanceLy( r.X, r.Y, r.Z, x, y, z ) < r.RadiusLy )
                       ?.NeedsPermit ?? false ) );
        }
    }

    internal class PermitRegion
    {
        internal string RegionName { get; set; }
        internal bool NeedsPermit { get; }

        // The coordinates for the center of the region
        internal decimal X { get; }
        internal decimal Y { get; }
        internal decimal Z { get; }

        // The radius of the region
        internal decimal RadiusLy { get; }

        internal PermitRegion ( string regionName, decimal x, decimal y, decimal z, decimal radiusLy, bool needsPermit )
        {
            this.RegionName = regionName;
            this.X = x;
            this.Y = y;
            this.Z = z;
            this.RadiusLy = radiusLy;
            this.NeedsPermit = needsPermit;
        }
    }
